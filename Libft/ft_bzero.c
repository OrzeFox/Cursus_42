#include "libft.h"

void	*ft_bzero(void *ptr, size_t n)
{
	unsigned int	i;
	char			*p;

	i = 0;
	p = (char *)ptr;
	while (n > i)
	{
		p[i] = '\0';
		i++;
	}
	return (p);
}
