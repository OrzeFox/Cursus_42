#include "libft.h"

void *ft_bzero(void *ptr, size_t n)
{
    unsigned int i = 0;
    char *p;
    p =(char *)ptr;
    while (n > i)
    {
        p[i] = '\0';
        i++;
    }
    
    return (p);
}